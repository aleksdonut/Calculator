package pl.calculator.logic;

public class CalculatorLogic {
    private int add(int x, int y) {
        return x + y;
    }

    private int subtract(int x, int y) {
        return x - y;
    }

    private int multiply(int x, int y) {
        return x * y;
    }

    private int divide(int x, int y) {
        return x / y;
    }
}
